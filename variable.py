# variables are case sensitive
a=100
A=200
print(a)
print(A)

# variable names cannot start with a number
# variable names can start with underscore character
_myvar=100
print(_myvar)

# camel case
myVariableName=100
print(myVariableName)

# pascal case
MyVariableName=100
print(MyVariableName)

# snake case
my_variable_name=100
print(my_variable_name)

# output many variables
